fun main(){

    // perkalian
    val result1 = 5 * 5
    println(result1)

    // pembagian
    val result2 = 10 / 2
    println(result2)

    // pengurangan
    val result3 = 5 - 2
    println(result3)

    // penjumlahan
    val result4 = 4 + 8
    println(result4)

    // modulus
    val result5 = 13 % 4
    println(result5)
}